# Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.

num = int(input("Digite um número: "))

dobro = num * 2
triplo = num * 3
raiz = num ** (1/2)

print(f"O número digitado foi {num}, seu dobro é {dobro}, seu triplo é {triplo} e sua raiz quadrada é {raiz}.")
